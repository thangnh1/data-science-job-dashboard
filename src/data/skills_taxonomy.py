def get_all_keywords() -> dict[str, str]:
    """Build a flat map from every lowercase keyword alias to its canonical skill name.

    Iterates over all categories and skills in SKILLS_TAXONOMY and constructs
    a single dictionary keyed by lowercase alias strings. Raises ValueError
    immediately if a duplicate alias is found (fail-fast).

    Returns:
        A dict mapping each lowercase keyword alias to its canonical skill name.

    Raises:
        ValueError: If the same keyword alias appears under two different
            canonical skills, indicating a taxonomy data error.
    """
    keyword_map: dict[str, str] = {}
    for _category, skills in SKILLS_TAXONOMY.items():
        for canonical_name, aliases in skills.items():
            for alias in aliases:
                if alias in keyword_map:
                    raise ValueError(
                        f"Duplicate keyword alias '{alias}' found: "
                        f"already mapped to '{keyword_map[alias]}', "
                        f"cannot also map to '{canonical_name}'"
                    )
                keyword_map[alias] = canonical_name
    return keyword_map


def get_categories() -> list[str]:
    """Return the list of top-level category names in the skills taxonomy.

    Returns:
        A list of category name strings, in definition order.
    """
    return list(SKILLS_TAXONOMY.keys())
